#include "libft.h"

int	ft_count_num(int n)
{
	int				i;
	unsigned int	neg;

	i = 0;
	if (n < 0)
		neg = -n;
	else
		neg = n;
	while (neg > 0)
	{
		neg /= 10;
		i++;
	}
	return (i);
}
